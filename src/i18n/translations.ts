export function getTranslation(
  lang: Language,
  key: string,
  defaultValue: string = "",
): string {
  const keys = key.split(".");
  let value: any = translations[lang];

  for (const k of keys) {
    value = value?.[k];
    if (value === undefined) return defaultValue;
  }

  return typeof value === "string" ? value : defaultValue;
}
